def center_toplevel_in_screen ( f_widget ):

    #/ nome do metodo (logger)
    #/ --------------------------------------------------------------------------------------------
    #l_szMetodo = "tkUtils::center_toplevel_in_screen"


    #** -------------------------------------------------------------------------------------------
    #*  m.poirot logger
    #*/
    #logger = logging.getLogger ( l_szMetodo )
    #logger.setLevel ( logging.INFO )
    #logger.debug ( ">> " )

    #** -------------------------------------------------------------------------------------------
    #*  obtem o toplevel
    #*/
    l_top = f_widget.winfo_toplevel ()

    #** -------------------------------------------------------------------------------------------
    #*  obtem a largura e altura da tela
    #*/
    l_sw = l_top.winfo_screenwidth ()
    l_sh = l_top.winfo_screenheight ()

    #** -------------------------------------------------------------------------------------------
    #*  obtem a largura e altura da janela
    #*/
    l_w = l_top.winfo_width ()
    l_h = l_top.winfo_height ()

    #** -------------------------------------------------------------------------------------------
    #*  calcula a nova posicao NO da janela
    #*/
    l_x = ( l_sw - l_w ) / 2
    l_y = ( l_sh - l_h ) / 2

    #** -------------------------------------------------------------------------------------------
    #*  configura tamanho e posicao da janela
    #*/
    l_top.geometry ( "%dx%d+%d+%d" % ( l_w, l_h, l_x, l_y ))

    #** -------------------------------------------------------------------------------------------
    #*  m.poirot logger
    #*/
    #logger.debug ( "<< " )

